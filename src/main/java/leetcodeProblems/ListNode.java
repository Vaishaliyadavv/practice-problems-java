package leetcodeProblems;

//https://leetcode.com/problems/remove-nth-node-from-end-of-list/submissions/1196623028/
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
